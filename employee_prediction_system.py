"""Feature Selection"""

x=df.drop('left',axis=1)  #all columns except target
y=df['left']         #target column

x

y

"""Encoding (categorical column into numerical)"""

x = pd.get_dummies(x , columns=['salary', 'Department'], drop_first=True)

x

"""Train-Test Split"""

from sklearn.model_selection import train_test_split

x_train, x_test, y_train, y_test = train_test_split(
    x, y, test_size=0.2, random_state=42,stratify=y
)

"""Model Evaluation"""

from sklearn.ensemble import RandomForestClassifier

# Create model
rf_model = RandomForestClassifier(
    n_estimators=101,
    max_depth=10,
    min_samples_split=5,
    min_samples_leaf=3,
    class_weight='balanced',
    random_state=42
)

from sklearn.model_selection import StratifiedKFold, cross_val_score

skf = StratifiedKFold(n_splits=5, shuffle=True, random_state=42)

cv_scores = cross_val_score(rf_model, x, y, cv=skf)

print("CV Accuracy:", cv_scores.mean())

rf_model.fit(x_train, y_train)

y_pred = rf_model.predict(x_test)

y_pred

from sklearn.metrics import accuracy_score,confusion_matrix,classification_report
print("Accuracy:", accuracy_score(y_test, y_pred))
cm=confusion_matrix(y_test, y_pred)
print(cm)
print(classification_report(y_test, y_pred))

sns.heatmap(cm, annot=True, fmt='d', cmap='Blues')
plt.xlabel("Predicted")
plt.ylabel("Actual")
plt.title("Confusion Matrix")
plt.show()

"""*Feature* Importance"""

feature_importance = pd.Series(rf_model.feature_importances_, index=x.columns)
feature_importance.sort_values(ascending=False).head(10)

